# to find element is member of list
# FUNCTIONS	: "in" "not in"

a=[1,45,67,9]
print(45 in a)# if 45 in the list , it becomes true, else false
print(25 in a)# if 25 in the list , it becomes true, else false
print(22 not in a)# if 22 not in the list , it becomes true else false
print(9 not in a)# if 9 not in the list , it becomes true else false