# print in range using list

n=list(range(20))# print value n-1(20-1 = 19)
print(n)
n=list(range(5,20))# 5--> start no / 20--> end no(consider till 19(n-1))
print(n)
n=list(range(2,10,2))#2--> start no / 10--> end no(consider till 9(n-1))/ 2--> add 2 to before no
print(n)
n=list(range(3,10,1))#3--> start no / 10--> end no(consider till 9(n-1))/ 1--> add 2 to before no
print(n)
print(list(range(3,10,1)))