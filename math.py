import math

print(math.sqrt(4)) #find square root 
print(math.ceil(2.88656))# to find highest round value
print(math.floor(2.758567))# to find lowest round value
print(math.factorial(6))#  to find factorial
print(math.fabs(-4))# to find absolute value ans:4
print(math.fabs(5))# to find absolute value ans:5
print(math.pow(3,2))# to calculate power value
print(math.log2(3))#to find log2 values
print(math.log10(3))#to find log10 values
print(math.pi)# to find pi value, use keyword "pi"